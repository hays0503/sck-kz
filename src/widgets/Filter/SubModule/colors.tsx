/**
 * Принимает строку с названием цвета/цветов (через запятую),
 * возвращает массив HEX-кодов соответствующих цветов.
 */
export function getHexColorsFromRussian(input: string): string[] {
  return input
    .toLowerCase()
    .split(',')
    .map(color => color.trim())
    .map(color => colorHexMap[color])
    .filter((hex): hex is string => Boolean(hex));
}

import React from 'react';

interface ColorBallProps {
  colors: string[]; // Массив цветов, передаваемых в компонент
}

const ColorBall: React.FC<ColorBallProps> = ({ colors }) => {
  // Функция для формирования стиля шарика с двумя цветами или одним
  const getBallStyle = () => {
    if (colors.length === 1) {
      return { backgroundColor: colors[0] }; // Один цвет
    }

    if (colors.length === 2) {
      // Два цвета: градиент
      return {
        background: `linear-gradient(45deg, ${colors[0]} 50%, ${colors[1]} 50%)`
      };
    }

    return {};
  };

  return (
    <div
      style={{
        width: '20px',
        height: '20px',
        borderRadius: '100%',
        border: '1px solid #ccc',
        display: 'inline-block',
        ...getBallStyle(),
      }}
    />
  );
};

export default ColorBall;
